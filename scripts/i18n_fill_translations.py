L10N_DIR = Path(__file__).resolve().parent.parent / "mobile" / "flutter" / "lib" / "l10n"
PLACEHOLDER_PREFIX = "[en] "


def main() -> int:
    arb_files = sorted(L10N_DIR.glob("app_*.arb"))
    if not arb_files:
        print(f"❌ no .arb files in {L10N_DIR}", file=sys.stderr)
        return 1

    total_patched = 0
    total_cleaned = 0
    for arb in arb_files:
        with arb.open("r", encoding="utf-8") as f:
            data = json.load(f)
        locale = arb.stem.removeprefix("app_")
        if locale == "en":
            continue
        patched = 0
        cleaned = 0
        for key, by_locale in T.items():
            if key not in data:
                continue
            cur = data[key]
            if not isinstance(cur, str):
                continue
            translation = by_locale.get(locale)
            if translation:
                if cur != translation:
                    data[key] = translation
                    patched += 1
            else:
                # No translation provided — strip the [en] marker and keep
                # the clean English value as a graceful fallback.
                if cur.startswith(PLACEHOLDER_PREFIX):
                    data[key] = cur[len(PLACEHOLDER_PREFIX):]
                    cleaned += 1
        # Also clean any keys that still carry the [en] marker.
        for k, v in list(data.items()):
            if isinstance(v, str) and v.startswith(PLACEHOLDER_PREFIX):
                data[k] = v[len(PLACEHOLDER_PREFIX):]
                cleaned += 1
        if patched or cleaned:
            with arb.open("w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
                f.write("\n")
        total_patched += patched
        total_cleaned += cleaned
        print(f"  {arb.name:30s} translated:{patched:>3}  cleaned:{cleaned:>3}")
    print(f"\nTotal: {total_patched} translated, {total_cleaned} [en]-prefix stripped.")
    print("Next: cd mobile/flutter && flutter gen-l10n")
    return 0


