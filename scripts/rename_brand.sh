#!/usr/bin/env bash
# Rename the FitWiz app brand across the codebase.
#
# Usage:
#   ./scripts/rename_brand.sh "FitWiz" "NewName"
#   ./scripts/rename_brand.sh "FitWiz" "NewName" "fitwiz.us" "newname.com"
#
# AFTER running this:
#   1. Verify the 3 constant files (sed already updated literal defaults):
#        - mobile/flutter/lib/core/constants/branding.dart
#        - backend/core/branding.py
#        - frontend/src/lib/branding.ts
#   2. cd mobile/flutter && flutter clean && flutter analyze && flutter test
#   3. cd backend && pytest tests/
#   4. cd frontend && npm run build
#   5. Commit, rebuild AAB, upload to Play Console.
#
# DOMAIN CHANGE: if you also pass old/new domain, you must separately:
#   - Buy the new domain
#   - Update Vercel project domain (Settings -> Domains)
#   - Update Resend "From" verified domain (Resend dashboard -> Domains)
#   - Move .well-known/assetlinks.json + apple-app-site-association to new domain
#   - Update Play Console listing privacy / terms / website / support URLs
#
# DOES NOT TOUCH (locked forever even on rename):
#   - Bundle/package IDs (com.aifitnesscoach.app)        — installs continuity
#   - Deep link scheme (fitwiz://)                       — user share links
#   - pubspec.yaml `name: fitwiz`                        — Drift .g.dart imports
#   - Native MethodChannel strings                       — must match registration
#   - Generated files (*.g.dart, *.freezed.dart)         — regenerated by build
#   - Lock files, node_modules, .venv, __pycache__       — vendored/cached
#
# DESIGN: case-sensitive name swap only (e.g. "FitWiz" -> "NewName"). Lowercase
# variants like "fitwiz://" (deep link scheme) and "package:fitwiz/" (Dart
# package name) are LOCKED and intentionally NOT swapped. The "fitwiz.us"
# domain is only swapped when you pass an explicit domain argument.

set -euo pipefail

OLD_NAME="${1:?Old name required (e.g. FitWiz)}"
NEW_NAME="${2:?New name required (e.g. Acme)}"
OLD_DOMAIN="${3:-}"
NEW_DOMAIN="${4:-}"

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$REPO_ROOT"

echo "Renaming '$OLD_NAME' -> '$NEW_NAME'..."
if [ -n "$OLD_DOMAIN" ]; then
  echo "Renaming domain '$OLD_DOMAIN' -> '$NEW_DOMAIN'..."
fi

# --- Specific config files (always covered) ----------------------------------
EXPLICIT_TARGETS=(
  # Flutter native configs
  "mobile/flutter/android/app/src/main/res/values/strings.xml"
  "mobile/flutter/android/app/src/main/AndroidManifest.xml"
  "mobile/flutter/android/app/src/main/res/xml/shortcuts.xml"
  "mobile/flutter/android/app/src/main/res/layout/widget_meal_suggestion.xml"
  "mobile/flutter/android/app/src/main/res/drawable/launch_background.xml"
  "mobile/flutter/android/app/src/main/res/drawable-v21/launch_background.xml"
  "mobile/flutter/ios/Runner/Info.plist"
  "mobile/flutter/ios/RunnerWidget/Info.plist"
  "mobile/flutter/android/keystores/README.md"
  "mobile/flutter/ios/FitWizLiveActivity/SETUP.md"

  # Constant files (sed updates the literal default values inside)
  "mobile/flutter/lib/core/constants/branding.dart"
  "mobile/flutter/lib/core/constants/app_links.dart"
  "backend/core/branding.py"
  "frontend/src/lib/branding.ts"
  "frontend/src/lib/links.ts"

  # Web static
  "frontend/index.html"
  "frontend/public/manifest.json"

  # Top-level docs
  "STORE_LISTING.md"
  "PLAY_STORE_LISTING_COPY.md"
  "TRADEMARK_TAKEDOWN_RESPONSE.md"
  "PLAY_LAUNCH_DAY.md"
  "PRE_REVIEW_SUBMISSION_GUIDE.md"
  "PLAY_STORE_CHECKLIST.md"
  "AUDIT.md"
  "IMPROVEMENTS_NEEDED.md"
  "WEB_ONBOARDING_FUNNEL.md"
  "CLAUDE.md"
  "UX_AUDIT_CHANGELOG.md"
  "mobile/flutter/CLAUDE.md"
)

# --- Recursive directories (all source files matching extensions) ------------
# These directories get a sweep across .py/.dart/.tsx/.ts/.html/.j2/.md.j2/.css
# files, with a hard-coded exclude list to avoid generated/locked files.
RECURSIVE_DIRS=(
  "backend"
  "mobile/flutter/lib"
  "mobile/flutter/test"
  "frontend/src"
  "wearos/app/src"
  "mobile/flutter/ios"
  "mobile/flutter/android/app/src"
)

EXCLUDE_PATTERNS=(
  "*/__pycache__/*"
  "*/.venv/*"
  "*/venv/*"
  "*/node_modules/*"
  "*/build/*"
  "*/dist/*"
  "*.g.dart"
  "*.freezed.dart"
  "*/migrations/*"
  "*.lock"
  "*.lockb"
)

build_find_args() {
  local dir="$1"; shift
  local ext_clause=""
  for ext in "$@"; do
    if [ -n "$ext_clause" ]; then
      ext_clause="$ext_clause -o"
    fi
    ext_clause="$ext_clause -name $ext"
  done
  local exclude_clause=""
  for ex in "${EXCLUDE_PATTERNS[@]}"; do
    exclude_clause="$exclude_clause -not -path $ex"
  done
  echo "$dir -type f \\( $ext_clause \\) $exclude_clause"
}

# Build the master file list
TARGET_FILES=()

# Add explicit targets that exist
for f in "${EXPLICIT_TARGETS[@]}"; do
  if [ -f "$f" ]; then
    TARGET_FILES+=("$f")
  fi
done

# Add recursive targets
for dir in "${RECURSIVE_DIRS[@]}"; do
  if [ -d "$dir" ]; then
    while IFS= read -r f; do
      TARGET_FILES+=("$f")
    done < <(
      find "$dir" -type f \
        \( -name "*.py" -o -name "*.dart" -o -name "*.tsx" -o -name "*.ts" \
           -o -name "*.html" -o -name "*.j2" -o -name "*.css" \
           -o -name "*.kt" -o -name "*.kts" -o -name "*.gradle" \
           -o -name "*.swift" -o -name "*.m" -o -name "*.h" \
           -o -name "*.xml" -o -name "*.plist" -o -name "*.json" -o -name "*.yml" \) \
        ! -path "*/__pycache__/*" \
        ! -path "*/.venv/*" \
        ! -path "*/venv/*" \
        ! -path "*/node_modules/*" \
        ! -path "*/build/*" \
        ! -path "*/dist/*" \
        ! -name "*.g.dart" \
        ! -name "*.freezed.dart" \
        ! -path "*/migrations/*" \
        ! -path "*/Pods/*" \
        ! -name "package-lock.json" \
        ! -name "yarn.lock" \
        ! -name "pnpm-lock.yaml" \
        ! -name "pubspec.lock"
    )
  fi
done

count=0
for f in "${TARGET_FILES[@]}"; do
  if [ ! -f "$f" ]; then continue; fi

  # Case-sensitive name swap (e.g. "FitWiz" -> "NewName")
  # Only this is universally safe. Lowercase variants like "fitwiz://" or
  # "package:fitwiz/" are LOCKED and not touched.
  if grep -q "$OLD_NAME" "$f" 2>/dev/null; then
    sed -i.bak "s/$OLD_NAME/$NEW_NAME/g" "$f"
    rm -f "$f.bak"
    count=$((count+1))
  fi

  # Domain swap if provided
  if [ -n "$OLD_DOMAIN" ] && [ -n "$NEW_DOMAIN" ]; then
    if grep -q "$OLD_DOMAIN" "$f" 2>/dev/null; then
      sed -i.bak "s|$OLD_DOMAIN|$NEW_DOMAIN|g" "$f"
      rm -f "$f.bak"
    fi
  fi
done

echo ""
echo "Sed processed ~$count files."
echo ""
echo "MANUALLY VERIFY the 3 constant files:"
echo "  - mobile/flutter/lib/core/constants/branding.dart  (appName, fullTitle)"
echo "  - backend/core/branding.py                          (APP_NAME, APP_FULL_TITLE)"
echo "  - frontend/src/lib/branding.ts                      (appName, fullTitle)"
echo ""
echo "Then run:"
echo "  cd mobile/flutter && flutter clean && flutter analyze && flutter test"
echo "  cd backend && pytest tests/"
echo "  cd frontend && npm run build"
