/*
 * Zealova — app-launch load test (k6)
 * ------------------------------------------------------------------
 * Simulates the REAL home-screen launch burst captured from the
 * 2026-05-16 Sentry incident: each virtual user fires ~50 endpoints
 * concurrently (the Flutter app's startup fan-out), then polls
 * /workouts/today every 5s — exactly the traffic shape that froze the
 * single-worker backend.
 *
 * This is the before/after yardstick for every phase of the scale-
 * hardening plan. Run it once to capture a BASELINE before any fix.
 *
 * USAGE
 *   k6 run -e STAGE=smoke  launch_burst.js          # 20 VUs, sanity
 *   k6 run -e STAGE=1k     launch_burst.js          # ramp to 1000
 *   k6 run -e STAGE=5k     launch_burst.js
 *   k6 run -e STAGE=10k    launch_burst.js          # the real target
 *
 *   -e BASE_URL=https://aifitnesscoach-zqi3.onrender.com   (default)
 *   -e TOKENS=tokens.json    pool of {user_id, token} — see gen_test_tokens.py
 *   -e SUMMARY=results/baseline.json   write a machine-readable summary
 *
 * IMPORTANT: 5k/10k against the production URL is itself a heavy load
 * test. Prefer pointing BASE_URL at a staging deploy, or coordinate a
 * low-traffic window. The harness never mutates real data beyond the
 * same POSTs the app itself sends on launch.
 */
import http from 'k6/http';
import { check, sleep } from 'k6';
import { Trend, Counter, Rate } from 'k6/metrics';
import { SharedArray } from 'k6/data';

// ---------------------------------------------------------------- config
const BASE_URL = (__ENV.BASE_URL || 'https://aifitnesscoach-zqi3.onrender.com').replace(/\/$/, '');
const API = `${BASE_URL}/api/v1`;
const STAGE = __ENV.STAGE || 'smoke';
const POLL_SECONDS = 5;          // app polls /workouts/today every ~5s
const POLLS_PER_VU = 4;          // 4 polls ≈ 20s on-screen, then VU recycles

// Token pool. Each entry: { "user_id": "...", "token": "<jwt>" }.
// Generated by gen_test_tokens.py. A small pool (100-500) cycled across
// VUs is standard practice — we are load-testing the server, not Supabase.
const TOKENS = new SharedArray('tokens', function () {
  const path = __ENV.TOKENS || 'tokens.json';
  try {
    const data = JSON.parse(open(path));
    if (!Array.isArray(data) || data.length === 0) {
      throw new Error('tokens file is empty');
    }
    return data;
  } catch (e) {
    throw new Error(
      `Could not load token pool from "${path}": ${e}. ` +
      `Run: python gen_test_tokens.py  (see README.md)`,
    );
  }
});

// VU ramp profiles. Each phase of the plan re-runs the SAME stage and
// compares p95 / error rate against the recorded baseline.
const STAGES = {
  smoke: [
    { duration: '20s', target: 20 },
    { duration: '40s', target: 20 },
    { duration: '10s', target: 0 },
  ],
  '1k': [
    { duration: '1m', target: 1000 },
    { duration: '3m', target: 1000 },
    { duration: '30s', target: 0 },
  ],
  '5k': [
    { duration: '2m', target: 5000 },
    { duration: '4m', target: 5000 },
    { duration: '1m', target: 0 },
  ],
  '10k': [
    { duration: '3m', target: 10000 },
    { duration: '5m', target: 10000 },
    { duration: '1m', target: 0 },
  ],
};

// ---------------------------------------------------------------- metrics
const burstDuration = new Trend('launch_burst_duration', true); // full fan-out wall time
const todayLatency = new Trend('workouts_today_latency', true);
const connTimeouts = new Counter('connection_timeouts');         // the incident signal
const httpErrors = new Counter('http_errors');                   // status 0 or >=500
const burstOk = new Rate('launch_burst_ok');                     // all-200 fan-out

export const options = {
  stages: STAGES[STAGE] || STAGES.smoke,
  // Generous timeout so a frozen server shows up as a slow request,
  // not a client-side abort that hides the real latency.
  setupTimeout: '60s',
  thresholds: {
    // A passing run after the fixes should satisfy all of these.
    'workouts_today_latency': ['p(95)<800'],
    'launch_burst_ok': ['rate>0.99'],
    'connection_timeouts': ['count<1'],
    'http_req_failed': ['rate<0.01'],
  },
  // Tag the discardable noise so the summary stays readable.
  summaryTrendStats: ['avg', 'min', 'med', 'p(90)', 'p(95)', 'p(99)', 'max'],
};

// --------------------------------------------------------------- endpoints
// The launch fan-out, reconstructed from the Sentry HTTP breadcrumb log.
// `{uid}` is replaced per-VU. Everything here is what the Flutter app
// actually fires within ~2s of cold launch.
function launchRequests(uid, headers) {
  const today = new Date().toISOString().slice(0, 10);
  const tz = 'America/Chicago';
  const q = `user_id=${uid}`;

  // GET fan-out — fired as one http.batch (mirrors the app's concurrency).
  const gets = [
    `/progress/xp/${uid}`,
    `/achievements/user/${uid}/streaks`,
    `/progress/trophies/${uid}/summary`,
    `/progress/trophies/${uid}/recent?limit=1`,
    `/progress/trophies/${uid}/pending-celebrations`,
    `/summaries/user/${uid}/latest`,
    `/skill-progressions/user/${uid}/summary`,
    `/leaderboard/unlock-status?${q}`,
    `/leaderboard/weekly-recap?board=xp`,
    `/leaderboard/discover?board=xp&scope=global`,
    `/leaderboard/rank?${q}`,
    `/consistency/insights?${q}&days_back=90`,
    `/consistency/calendar?${q}&weeks=4`,
    `/workouts/?${q}&allow_multiple_per_date=true`,
    `/workouts/today?${q}`,
    `/nutrition/food-logs/${uid}?limit=50&tz=${tz}`,
    `/nutrition/summary/daily/${uid}?date=${today}&tz=${tz}`,
    `/nutrition/preferences/${uid}`,
    `/nutrition/streak/${uid}`,
    `/nutrition/dynamic-targets/${uid}?date=${today}`,
    `/nutrition/weight-logs/${uid}?limit=30`,
    `/social/challenges/notifications?${q}&unread_only=false`,
    `/social/challenges?active_only=true&${q}`,
    `/social/feed/${uid}?page=1&page_size=20&sort_by=recent`,
    `/social/connections/followers/${uid}?limit=50`,
    `/social/connections/following/${uid}?limit=50`,
    `/social/connections/friends/${uid}`,
    `/notifications/billing/${uid}`,
    `/sauna/daily/${uid}`,
    `/habits/${uid}/today`,
    `/hydration/daily/${uid}?date_str=${today}`,
    `/fasting/preferences/${uid}`,
    `/gym-profiles/?${q}`,
    `/wrapped/summary`,
    `/timeline?${q}&days=1&limit=200`,
    `/library/branded-programs/current?${q}`,
    `/xp/unclaimed-crates`,
    `/xp/daily-goals-status?date=${today}`,
    `/xp/daily-crates?date=${today}`,
    `/xp/consumables`,
    `/xp/first-time-bonuses`,
    `/xp/active-events`,
    `/xp/weekly-checkpoints`,
    `/xp/monthly-achievements`,
    `/xp/level-ups/unacknowledged`,
  ];

  return gets.map((path) => ({
    method: 'GET',
    url: `${API}${path}`,
    params: { headers, tags: { name: tagOf(path) }, timeout: '30s' },
  }));
}

// Collapse a URL to a stable metric tag (drop ids + query) so per-endpoint
// percentiles aggregate instead of exploding into thousands of series.
function tagOf(path) {
  return path
    .split('?')[0]
    .replace(/\/[0-9a-f]{8}-[0-9a-f-]{27,}/gi, '/{id}')
    .replace(/\/\d+/g, '/{n}');
}

// ------------------------------------------------------------------ setup
export function setup() {
  // Fail fast if the backend is unreachable before spinning up VUs.
  const res = http.get(`${BASE_URL}/`, { timeout: '15s' });
  check(res, { 'backend reachable': (r) => r.status === 200 });
  return { startedAt: new Date().toISOString() };
}

// ------------------------------------------------------------------- main
export default function () {
  const acct = TOKENS[(__VU + __ITER) % TOKENS.length];
  const headers = {
    Authorization: `Bearer ${acct.token}`,
    'Content-Type': 'application/json',
    Accept: 'application/json',
  };
  const uid = acct.user_id;

  // --- 1. Launch burst: fire the whole fan-out at once -----------------
  const t0 = Date.now();
  const responses = http.batch(launchRequests(uid, headers));
  burstDuration.add(Date.now() - t0);

  let allOk = true;
  for (const r of responses) {
    if (r.status === 0) {
      // status 0 = client never got a response: connection/read timeout.
      connTimeouts.add(1);
      allOk = false;
    } else if (r.status >= 500) {
      httpErrors.add(1);
      allOk = false;
    } else if (r.status === 401) {
      // Expired token in the pool — not a server fault, but flag it so a
      // stale tokens.json doesn't masquerade as a green run.
      allOk = false;
    }
  }
  burstOk.add(allOk);

  // --- 2. Steady-state polling of /workouts/today ----------------------
  for (let i = 0; i < POLLS_PER_VU; i++) {
    sleep(POLL_SECONDS);
    const r = http.get(`${API}/workouts/today?user_id=${uid}`, {
      headers,
      tags: { name: 'GET /workouts/today' },
      timeout: '30s',
    });
    todayLatency.add(r.timings.duration);
    if (r.status === 0) connTimeouts.add(1);
    else if (r.status >= 500) httpErrors.add(1);
    check(r, { 'today poll ok': (x) => x.status === 200 });
  }
}

// --------------------------------------------------------------- teardown
export function handleSummary(data) {
  const out = { stdout: textSummary(data) };
  if (__ENV.SUMMARY) {
    out[__ENV.SUMMARY] = JSON.stringify(data, null, 2);
  }
  return out;
}

// Minimal text summary (k6's built-in is imported lazily to avoid a hard
// dependency on the jslib CDN in offline CI).
function textSummary(data) {
  const m = data.metrics;
  const g = (name, stat) => {
    const v = m[name] && m[name].values && m[name].values[stat];
    return v === undefined ? 'n/a' : Math.round(v * 100) / 100;
  };
  return [
    '',
    '=== Zealova launch-burst load test ===',
    `stage:                 ${STAGE}`,
    `base url:              ${BASE_URL}`,
    `token pool size:       ${TOKENS.length}`,
    '',
    `launch burst wall ms:  avg ${g('launch_burst_duration', 'avg')}  p95 ${g('launch_burst_duration', 'p(95)')}  p99 ${g('launch_burst_duration', 'p(99)')}`,
    `/workouts/today ms:    avg ${g('workouts_today_latency', 'avg')}  p95 ${g('workouts_today_latency', 'p(95)')}  p99 ${g('workouts_today_latency', 'p(99)')}`,
    `launch burst all-200:  ${g('launch_burst_ok', 'rate') * 100}%`,
    `connection timeouts:   ${g('connection_timeouts', 'count')}   <-- the incident signal`,
    `http 5xx / status 0:   ${g('http_errors', 'count')}`,
    `http_req_failed rate:  ${g('http_req_failed', 'rate')}`,
    `http_req_duration p95: ${g('http_req_duration', 'p(95)')} ms`,
    '======================================',
    '',
  ].join('\n');
}
